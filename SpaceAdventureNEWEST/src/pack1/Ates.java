package pack1;

public class Ates {
	
	private int atesX;
	private int atesY;
	
	public Ates(int atesX, int atesY) {
		super();
		this.atesX = atesX;
		this.atesY = atesY;
	}
	
	public int getAtesX() {
		return atesX;
	}

	public void setAtesX(int atesX) {
		this.atesX = atesX;
	}

	public int getAtesY() {
		return atesY;
	}

	public void setAtesY(int atesY) {
		this.atesY = atesY;
	}


	
	

}
